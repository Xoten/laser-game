package model;

public class User {
  
	
     private int Score;
     private String nickname;
      
      
      public User(String n) {
    	  nickname = n;
    	  
    	  
      }
      
      
      
      public int getScore() {
		return Score;
	}
	public void setScore(int score) {
		Score = score;
	}
	public String getNickname() {
		return nickname;
	}
	
	
      
      
      
   
      
      
      
      
      
    
	
	
	
}
